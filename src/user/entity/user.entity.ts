import { Column, CreateDateColumn, PrimaryGeneratedColumn } from 'typeorm';

class User {
  @PrimaryGeneratedColumn('uuid')
  id: string;

  @Column()
  name: string;

  @Column({ unique: true })
  email: string;

  @Column()
  password: string;

  @CreateDateColumn()
  createdAt: Date;
}

export default User;
